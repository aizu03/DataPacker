namespace Tester.Path
{
    public struct Simple
    {
        public string name;
        public int age;
        public int id;
        public string description;

        public Simple(string name, int age, int id, string description)
        {
            this.name = name;
            this.age = age;
            this.id = id;
            this.description = description;
        }
    }
